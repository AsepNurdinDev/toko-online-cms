    <script>
        document.addEventListener('DOMContentLoaded', function () {
            // --- 1. Fungsi Accordion FAQ ---
            const triggers = document.querySelectorAll('.accordion-trigger');
            triggers.forEach(trigger => {
                trigger.addEventListener('click', function () {
                    const panel = this.nextElementSibling;
                    const icon = this.querySelector('.accordion-icon');
                    const item = this.parentElement;
                    
                    document.querySelectorAll('.accordion-panel').forEach(p => {
                        if (p !== panel && p.style.maxHeight) {
                            p.style.maxHeight = null;
                            p.previousElementSibling.querySelector('.accordion-icon').style.transform = 'rotate(0deg)';
                            p.parentElement.classList.remove('border-neutral-400');
                        }
                    });

                    if (panel.style.maxHeight) {
                        panel.style.maxHeight = null;
                        icon.style.transform = 'rotate(0deg)';
                        item.classList.remove('border-neutral-400');
                    } else {
                        panel.style.maxHeight = panel.scrollHeight + "px";
                        icon.style.transform = 'rotate(45deg)';
                        item.classList.add('border-neutral-400');
                    }
                });
            });

            // --- 2. Fungsi Efek Scroll Reveal ---
            const reveals = document.querySelectorAll('.reveal');
            const revealOnScroll = () => {
                const windowHeight = window.innerHeight;
                reveals.forEach(el => {
                    const elementTop = el.getBoundingClientRect().top;
                    const elementVisible = 100;
                    if (elementTop < windowHeight - elementVisible) {
                        el.classList.remove('opacity-0', 'translate-y-4');
                        el.classList.add('opacity-100', 'translate-y-0');
                    }
                });
            };
            window.addEventListener('scroll', revealOnScroll);
            revealOnScroll();
        });
    </script>
